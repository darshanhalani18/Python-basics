""""
Arithmetic Opearators => + , - , * , / (divison), // (floor division),
                        % , ** (power/exponent)
"""
print(5+2)
print(4-2)
print(5*5)
print(5 % 2)

# Divison (/) operator returns a floating-point result.
print(3/2)  # 1
print(2/2)  # 1.0

# Floor divison (//) operator divides two numbers and returns the integer part of the quotient, rounding down to the nearest whole number.

print(3//2)  # 1
print(4//2)   # 2

# Power or Exponentiation (**) :
print(3**2)  # 9
