'''
 In python data types are considered as classes and the variable
 we create those are known as instance or objects of these classes.
'''
var1 = 10
var2 = 10.20
print(type(var1))   # <class 'int'>
print(type(var2))   # <class 'float'>
var3 = "Hello"
print(type(var3))   # <class 'str'>
var4 = 0b10  # here 0b is prefix for binary numbers
print(var4)  # 2
print(var3[0])
var5 = True
print(type(var5))   # <class 'bool'>
